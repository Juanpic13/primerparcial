# ejercicio 2: Escribe un programa que calcule el área y el perímetro de un cuadrado
# y muestre los resultados (indicando cuál es cuál) por pantalla.

"le pedimos al usuario los datos "
lado = float(input("Ingrese la medida de un lado del cuadrado: "))
area = lado * lado 
perimetro = lado * 4 
"se le muestra al usuario los resultados obtenidos"
print("El área del cuadrado es: ", area, "y su perimetro", perimetro) 

